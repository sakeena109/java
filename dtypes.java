public class dtypes {
    public static void main(String[] args) {
        int a=10;
        float b=10.123456f;
        double c=28.9;
        short d=10;
        long e=99967;
        String f="sakeena";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}